a = 0.4
for i in range(120):
    if (0<=i<30 or 60<=i<90):
        a = (a + 0.08)
        # 向左转3度
        t.left(3)
        #    向前走a的步长
        t.forward(a)
    else:
        a = (a - 0.08)
        t.left(3)
        t.forward(a)
# 依据轮廓填充
t.end_fill()
